//Создайте функцию avg() , которая будет находить среднее значение по всем своим аргументам (аргументы величины числовые).
function avg() {
    let sum = 0;
    for (let i = 0; i < arguments.length; i++) {
        sum += arguments[i];
    }
    return sum / arguments.length;
}
console.log(avg(1, 2, 3, 4)); 



